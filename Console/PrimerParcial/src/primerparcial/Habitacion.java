package primerparcial;

public class Habitacion {
	float ancho;
	float largo;
	
	public void setancho(float ancho) {
		this.ancho = ancho;
	}
	
	public float getancho() {
		return ancho;
	}
	
	public void setlargo(float largo) {
		this.largo = largo;
	}
	
	public float getlargo() {
		return largo;
	}
	

}
